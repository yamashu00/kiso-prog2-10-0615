/*
 * ★ ボーナス問題（発展）
 *
 * 問題: 3 以上 9999 以下の奇数 a で、
 *       a^2 - a が 10000 で割り切れるものを全て求めよ。
 *
 * 出力形式: 見つかった数を 1行に 1つ表示する
 *
 * コンパイル: gcc bonus_odd_divisible.c -o bonus
 * 実行:       ./bonus
 *
 * ─────────────────────────────────────────────────────────
 * 【解答パターンは自由です。いくつか例を示します。】
 *
 * パターンA: 直接計算
 *   a^2 - a をそのまま計算して 10000 で割り切れるか確認する
 *   条件式: (a * a - a) % 10000 == 0
 *
 * パターンB: 因数分解を使う
 *   a^2 - a = a(a-1) と書き換える
 *   条件式: a * (a - 1) が 10000 の倍数かどうかを確認する
 *
 * パターンC: 数学的に絞り込む
 *   a は奇数なので a-1 は必ず偶数。
 *   10000 = 16 × 625 に分解して、それぞれの条件を整理する。
 *   「a が 625 の倍数」かつ「a-1 が 16 の倍数」→ 条件式に変換する
 *
 * パターンD: 数学的に答えを導き、結果だけ printf する
 *   コメントに「なぜその答えになるか」の説明を書くこと
 * ─────────────────────────────────────────────────────────
 */

