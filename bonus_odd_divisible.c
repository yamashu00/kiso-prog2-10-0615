/*
 * ★ ボーナス問題（発展）
 *
 * 問題: 3 以上 9999 以下の奇数 a で、
 *       a^2 - a が 10000 で割り切れるものを全て求めよ。
 */

// 自分の名前: 今泉 心寧

#include <stdio.h>

int main(void) {
    int a;

    /*
     * a は 3以上9999以下の奇数なので、
     * for文では 3 から始めて、2ずつ増やす。
     *
     * a^2 - a が 10000 で割り切れるかは、
     * (a * a - a) % 10000 == 0
     * で判定できる。
     *
     * % は余りを求める演算子なので、
     * 余りが 0 なら 10000 で割り切れるという意味になる。
     */
    for (a = 3; a <= 9999; a += 2) {
        if ((a * a - a) % 10000 == 0) {
            printf("%d\n", a);
        }
    }

    return 0;
}
